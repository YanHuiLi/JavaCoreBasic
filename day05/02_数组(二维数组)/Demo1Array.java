class Demo1Array {
	public static void main(String[] args) {
		int x = 10;
		int[] arr1 = new int[5];			//一维数组
		int[][] arr2 = new int[5][3];		//二维数组
		/*
		二维数组
		这是一个二维数组
		这个二维数组中有五个一维数组
		每个一维数组中有三个元素
		*/
		int[][][] arr3 = new int[3][4][5];
		/*
		这是一个三维数组
		这个三维数组中有3个二维数组
		每个二维数组中有4个一维数组
		每个一维数组中有5个元素
		*/

		/*
		* 1:数据类型 数组名[][] = new 数据类型[m][n];
		* 2:数据类型[] 数组名[] = new 数据类型[m][n];
		*/

		int arr4 [][] = new int[3][4];
		int[] arr5 [] = new int[5][6];
	}
}
